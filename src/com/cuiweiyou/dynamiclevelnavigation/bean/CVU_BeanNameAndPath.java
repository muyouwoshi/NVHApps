package com.cuiweiyou.dynamiclevelnavigation.bean;

/**
 * <b>类名</b>: BeanNameAndPath，<br/>
 * <b>说明</b>: 文件名，创建日期，绝对路径 <br/>
 */
public class CVU_BeanNameAndPath {

	private String name;
	private String createTime;
	private String path;

	/** 
	 * 
	 * @param name 从文件名提取的显示名称
	 * @param createTime 从文件名提取的创建日期
	 * @param path 文件全路径
	 * */
	public CVU_BeanNameAndPath(String name, String createTime, String path) {
		super();
		this.name = name;
		this.createTime = createTime;
		this.path = path;
	}

	public CVU_BeanNameAndPath() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	@Override
	public String toString() {
		return "BeanNameAndPath [name=" + name + ", createTime=" + createTime + ", path=" + path + "]";
	}

}
